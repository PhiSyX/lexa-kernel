#[macro_export]
macro_rules! import {
	($($vis:vis $name:ident,)*) => {
		$($vis mod $name;)*
	};

	($($vis:vis $directory:ident / { $($name:ident,)* };)*) => {
		$($vis mod $directory { $($vis mod $name ;)* })*
	}
}

#[macro_export]
macro_rules! public_import {
	($($name:ident,)*) => {
		$crate::import! { $(pub $name,)* }
	};

	($($directory:ident / { $($name:ident,)* };)*) => {
		$crate::import! { $(pub $directory / { $($name,)* };)* }
	}
}
